unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	char_num;
	unsigned int	index;

	char_num = 0;
	index = 0;
	while (src[char_num] != '\0')
		char_num++;
	if (size != 0)
	{
		while (src[index] != '\0' && index < (size - 1))
		{
			dest[index] = src[index];
			index++;
		}
		dest[index] = '\0';
	}
	return (char_num);
}
