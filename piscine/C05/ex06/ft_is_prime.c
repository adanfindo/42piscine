int	ft_is_prime(int nb)
{
	int	index;

	if (nb == 2)
		return (1);
	if (nb % 2 == 0 || nb == 1 || nb == 0)
		return (0);
	index = (nb - 1) / 2;
	while (index > 1)
	{
		if (nb % index == 0)
			return (0);
		index--;
	}
	return (1);
}
